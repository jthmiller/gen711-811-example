### Lake Tahoe Air Vapor (AIR) and Water samples (WLW). The goal here is to compare the cyanobacterial communities in the air and water at Lake Tahoe. The samples are collected from three different locations around the lake: Emerald Bay (EW), Tahoe Keys (TK), and Vikingsholm Point (VP). The AIR samples are collected from the air, while the WLW samples are collected from the water. The sample names are as follows:
AIR-EW
AIR-TK
AIR-VP

WLW-EW
WLW-TK
WLW-VP

### Control samples:
NTC, no template control
PAC, positive amplification control, ZymoBIOMICS Microbial Community Standard
XB, extraction blank, a negative control for the DNA extraction process. It is used to detect any contamination that may have occurred during the DNA extraction step. The XB should not contain any DNA, and if it does, it indicates that there may have been contamination during the extraction process.

